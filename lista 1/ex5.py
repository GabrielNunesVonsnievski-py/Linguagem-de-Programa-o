"5) Faça um programa que leia um valor em metros e o exiba convertido em centímetros e milímetros"
metros = int(input("Digite um valor em Metros: "))
centimetros = metros /100
milimetros = metros /1000

print(f"O mesmo valor em centímetros é:",centimetros)
print(f"O mesmo valor em milímetros é:",milimetros)
